/*
Seguindo com o conceito de variável,posso usar uma variável,
para concatenar(juntar), duas variáveis do tipo String(texto).
*/
//Abro o bloco de código com uma função principal...
void main(){

  //Isso não é uma operação matemática
  //isso é uma concatenação

  //concateno duas palavras em uma variável
  var total = 'Olá ' + 'mundão';

  //Declaro mais duas variáveis
  var nome1 = "Ramon ";
  var nome2 =  "Abreu";

//concateno duas variáveis em uma outra variável
  var nomeFinal = nome1 + nome2;

  //imprimo no console a primeira concatenação
  print(total);
  //imprimo no console a primeira concatenação
  print(nomeFinal);

  //Lembrar do ; no final de cada instução!!!

}
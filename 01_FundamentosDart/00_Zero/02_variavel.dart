//Seguindo o conceito anterior...
main(){

  /*Declaramos uma variável por meio da palavra Var,
    depois seguida damos um nome para ela : hello,
    em seguida atibuimos um valor para hello.
  */
  var hello = "Olá mundo !";

      //Palavras  (Strings) é necessário usar " " 



      /*Você pode perceber que a variável  hello assume
        um outro valor caso seja alterada,esse é o fundamento
        de uma variável, poder ter valores alterado de acordo 
        com a situação dentro do código*/

      hello = "Tamo aí no mundão!";


      /*Usando o mesmo conceito para outra variável,
        Números Não precisa de usar  " "
      */
      var numero = 15;
  print(hello);
  print(numero);
}